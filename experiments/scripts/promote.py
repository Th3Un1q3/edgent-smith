#!/usr/bin/env python3
"""Promote an accepted experiment candidate to new champion baseline.

Usage:
    python experiments/scripts/promote.py --name <experiment-name>

Prerequisites:
    - Manifest must have decision=accept
    - All three eval stages (smoke, benchmark, holdout) must have passed

Outputs:
    - Updates experiments/baselines/current.json with candidate results
    - Prints a promotion PR description to stdout
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
from _common import BASELINES_DIR, MANIFESTS_DIR, fail, now_iso


def main() -> None:
    parser = argparse.ArgumentParser(description="Promote accepted experiment to champion")
    parser.add_argument("--name", required=True, help="Experiment name")
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Print PR description without updating baseline",
    )
    args = parser.parse_args()

    manifest_file = MANIFESTS_DIR / f"{args.name}.json"
    if not manifest_file.exists():
        fail(f"No manifest: {manifest_file}")

    manifest = json.loads(manifest_file.read_text())

    if manifest.get("decision") != "accept":
        fail(
            f"Experiment '{args.name}' has decision='{manifest.get('decision')}'. "
            "Only accepted experiments can be promoted."
        )

    # Build promotion summary
    smoke = manifest.get("smoke_result", {}) or {}
    benchmark = manifest.get("benchmark_result", {}) or {}
    holdout = manifest.get("holdout_result", {}) or {}

    def _row(label: str, suite: dict) -> str:  # type: ignore[type-arg]
        pr = suite.get("pass_rate", "N/A")
        cs = suite.get("composite_score", "N/A")
        lat = suite.get("avg_latency_seconds", "N/A")
        pr_s = f"{pr:.2%}" if isinstance(pr, float) else str(pr)
        cs_s = f"{cs:.4f}" if isinstance(cs, float) else str(cs)
        lat_s = f"{lat:.2f}s" if isinstance(lat, float) else str(lat)
        return f"| {label:<9} | {pr_s:<9} | {cs_s:<15} | {lat_s:<11} |"

    table_rows = "\n".join([
        _row("Smoke", smoke),
        _row("Benchmark", benchmark),
        _row("Holdout", holdout),
    ])
    mutation_list = "\n".join(
        f"- `{s}`" for s in manifest.get("mutation_surface", [])
    )

    pr_description = (
        f"## Experiment Promotion: `{args.name}`\n\n"
        f"**Hypothesis:** {manifest.get('hypothesis', 'N/A')}\n\n"
        f"**Mutation surface:**\n{mutation_list}\n\n"
        "**Eval results:**\n\n"
        "| Suite     | Pass Rate | Composite Score | Avg Latency |\n"
        "|-----------|-----------|-----------------|-------------|\n"
        f"{table_rows}\n\n"
        f"**Decision:** ACCEPT – {manifest.get('rationale', '')}\n\n"
        f"**Promoted at:** {now_iso()}\n\n"
        "---\n"
        "*Generated by `experiments/scripts/promote.py`*\n"
    )

    print(pr_description)

    if args.dry_run:
        print("[dry-run] Skipping baseline update.")
        return

    # Update baseline
    new_baseline = {
        "registered_at": now_iso(),
        "promoted_from": args.name,
        "suite": "all",
        "results": {
            k: manifest.get(f"{k}_result")
            for k in ("smoke", "benchmark", "holdout")
            if manifest.get(f"{k}_result")
        },
    }

    BASELINES_DIR.mkdir(parents=True, exist_ok=True)
    baseline_file = BASELINES_DIR / "current.json"
    baseline_file.write_text(json.dumps(new_baseline, indent=2))
    print(f"Baseline updated: {baseline_file}")

    manifest["status"] = "promoted"
    manifest_file.write_text(json.dumps(manifest, indent=2))
    print("Manifest updated: promoted")


if __name__ == "__main__":
    main()
